package midgard.utils;

/**
 *
 * @author fenrrir
 */
public class NumericUtils {

    public static boolean lt(double a, double b){
        return a < b;
    }

    public static boolean gt(double a, double b){
        return a > b;
    }

    public static boolean eq(double a, double b){
        return a == b;
    }

}
